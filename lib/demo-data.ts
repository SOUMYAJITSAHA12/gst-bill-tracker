export function getDemoStats(invoices: Invoice[]): DashboardStats {
  const matched = invoices.filter((i) => i.is_matched);
  return {
    totalInvoices: invoices.length,
    matchedCount: matched.length,
    unmatchedCount: invoices.length - matched.length,
    totalItc: invoices.reduce((s, i) => s + i.igst + i.cgst + i.sgst, 0),
    totalTaxableValue: invoices.reduce((s, i) => s + i.taxable_value, 0),
  };
}
